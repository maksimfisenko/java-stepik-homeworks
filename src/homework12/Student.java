public class Student {
    int studentId;
    String name;
    String surname;
    int yearOfStudying;

    public Student(int studentId, String name, String surname, int yearOfStudying) {
        this.studentId = studentId;
        this.name = name;
        this.surname = surname;
        this.yearOfStudying = yearOfStudying;
    }
}

package homework6;

public class Calculator {
    public void calculateSum() {
        System.out.println("Output: " + 0);
    }
    public void calculateSum(int n1) {
        int output = n1;
        System.out.println("Output: " + output);
    }
    public void calculateSum(int n1, int n2) {
        int output = n1 + n2;
        System.out.println("Output: " + output);
    }
    public void calculateSum(int n1, int n2, int n3) {
        int output = n1 + n2 + n3;
        System.out.println("Output: " + output);
    }
    public void calculateSum(int n1, int n2, int n3, int n4) {
        int output = n1 + n2 + n3 + n4;
        System.out.println("Output: " + output);
    }
    public void calculateSum(int n1, int n2, int n3, int n4, int n5) {
        int output = n1 + n2 + n3 + n4 + n5;
        System.out.println("Output: " + output);
    }
}
